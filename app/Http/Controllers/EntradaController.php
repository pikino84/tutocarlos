     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(EntradaFormRequest $request)
    {
        
        $entrada = new Entrada;
        $entrada->titulo = $request->input('titulo');
        $entrada->contenido = $request->input('contenido');
        $entrada->user_id = 1;
        $entrada->save();
        return $entrada->id;
    }

